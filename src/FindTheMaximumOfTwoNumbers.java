public class FindTheMaximumOfTwoNumbers {
    public static void main(String[] args){
        int a=100;
        int b=700;

        if(a > b){
            System.out.println("Max num is:"+a);
        }
        else if(b > a){
            System.out.println("Max num is:"+b);
        }
        else{
            System.out.println("Both are equal");
        }
    }
}
